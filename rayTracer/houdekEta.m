%
% HOUDEKETA Sample eta(ni) from fig.3 of Houdek's paper.
%
% eta = houdekEta(ni)

function eta = houdekEta(ni)

  etaOfNi = getEtaOfNi();
  ETA = etaOfNi(:,2) * 10^-6;
  NI = etaOfNi(:,1)*10^-3;
  
  if sum((ni < NI(1)) | (ni > NI(end))) > 0; error 'ni is out of bounds';
  end
  
  eta = interp1(NI, ETA, ni, 'linear');  

